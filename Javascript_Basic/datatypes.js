// Declaring string variables
var firstName = "Sifat";       // firstName is a string
var lastName = "Samin";        // lastName is a string
var country = "Bangladesh";    // country is a string

// Printing string variables
console.log(firstName);        // Output: Sifat
console.log(lastName);         // Output: Samin
console.log(country);          // Output: Bangladesh

// Checking data types of string variables
console.log(typeof firstName); // Output: string
console.log(typeof country);   // Output: string

// Declaring boolean variables
var isTrue = true;             // isTrue holds a boolean value: true
var isFalse = false;           // isFalse holds a boolean value: false

// Printing boolean variables
console.log(isTrue);           // Output: true
console.log(isFalse);          // Output: false

// Checking data types of boolean variables
console.log(typeof isTrue);    // Output: boolean
console.log(typeof isFalse);   // Output: boolean

// Declaring number variables (integer and float)
var a = 10;                    // a is an integer number
var b = 20.60;                 // b is a floating-point number
var c = 534;                   // c is an integer number

// Printing number variables
console.log(a);                // Output: 10
console.log(b);                // Output: 20.6
console.log(c);                // Output: 534

// Checking data types of number variables
console.log(typeof a);         // Output: number
console.log(typeof b);         // Output: number
console.log(typeof c);         // Output: number
